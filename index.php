    <h1>Dirt Rally - online rally manager</h1>
    <br />
    <p align="center" class="middle justify">
<strong>What it's all about?</strong><br />

The main idea of the game - to manage virtual rally team and win the championship in the highest group “World Class”.
All registered users become virtual managers, who has the rights to manage rally teams, meaning that you have to deal with: cars, finances, hiring drivers / co-drivers.<br><br>
The game has teams by default, these teams are managed automatically by “robots / not real users”. These teams have amateur drivers and co-drivers. Once signed up, new managers are be able to give a new name to their team. Also, all teams have 2 different cars, drivers, co-drivers, mechanics. Cars have 1st level parts. Car types: 4WD and 2WD. Parts could be upgraded up to 10th level.<br><br>
How to survive during the season? Money are being transfered to all teams after every rally event dependent on their rally results. Money from sponsors are also available. Teams have to pay salaries to it’s crew: drivers, co-drivers, mechanics. Cars can be damaged a lot during the rally events, so you have to give orders which parts must be repaired by your team's mechanics. In order to get better results, managers have to train their crew every week.<br><br>
Every season lasts 15 weeks, meaning 15 different rally events. One full rally even lasts for 3 days as well as it is in the real world rally championship. On the 16th week, there is a race called “Race of Champions”, where winners of each group compete for the world title.<br><br>
